/* Inkrement a dekrement */
public class aritmeticke_operacie_4 {
    public static void main(String[] args) {
        
        // Inkrement (++);
        int number = 5;
        number++; // Zvýši hodnotu premennej o 1
            System.out.println("Hodnota po inkrementácii je : " + number); // Vypíše 6

        // Dekrement (--);
        int number_1 = 10;
        number_1--; // Zníži hodnotu premennej o 1
            System.out.println("Hodnota po dekrementácii je : " + number_1); // Vypíše 9

        // Inkrement a Dekrement s priradením
        int number_2 = 10;
        number_2 += 5; // Zvýši hodnotu premennej o 5
            System.out.println("Hodnota po zloženom príraste je : " + number_2); // Vypíše 15

    }
}
