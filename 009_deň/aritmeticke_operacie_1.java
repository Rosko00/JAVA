/* Aritmetické operácie */
public class aritmeticke_operacie_1 {
    public static void main(String[] args) {

        // Sčítanie
        int a = 5;
        int b = 3;
        int sum = a + b; // Suma 5 + 3
        System.out.println(sum);

        //Násobenie
        int x = 4;
        int y = 7;
        int product = x * y;
        System.out.println(product);

        //Zložené vyjadrenie
        int a_a = 10;
        int b_b = 5;
        int c_c = 3;
        int result = (a_a * b_b) + (a_a - c_c); // Výraz (10 * 5) + (10 - 3)
        System.out.println(result);    
    }
}
