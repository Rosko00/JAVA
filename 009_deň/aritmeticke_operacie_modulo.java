public class aritmeticke_operacie_modulo {
    public static void main(String[] args) {
        //Operátor modulo
        System.out.println("Modulo zbytok po delení:");

        int x = 42;
        double y = 42.25;
        System.out.println("x modulo 10 = " + x % 10);
        System.out.println("Y modulo 10 = " + y % 10);
    }
}
