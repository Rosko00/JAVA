/* Použitie kľúčových slov class, public, static, a void */
public class klucove_slova_1 {
       public static void main(String[] args) {
        // Výpis textu na konzolu
        System.out.println("Marián Gálik");
    } 
}
// public: Prístupové kľúčové slovo označujúce, že trieda alebo metóda je prístupná z iných tried.
// class: Označuje definíciu triedy.
// static: Označuje, že metóda main patrí triede, nie inštancii triedy.
// void: Označuje, že metóda main nevracia žiadnu hodnotu.