/* Použitie bodkončiarok */
public class oddelovace_1 {
       public static void main(String[] args) {
        // Deklarácia a inicializácia premenných oddelených bodkočiarkami
        int a = 5;
        int b = 10;
        int sum = a + b;
        
        // Výpis hodnoty sum na konzolu
        System.out.println("Súčet: " + sum);
    } 
}
