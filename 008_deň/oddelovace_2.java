/* Použitie čiarok */
public class oddelovace_2 {
        public static void main(String[] args) {
        // Deklarácia a inicializácia poľa s použitím čiarok na oddelenie prvkov
        int[] numbers = {1, 2, 3, 4, 5};
        
        // Cyklus for na prechod cez prvky poľa
        for (int number : numbers) {
            System.out.println("Číslo: " + number);
        }
    }
    
}
/* čiarky (',') sa používajú na oddelenie prvkov v poli, ako aj na oddelenie parametrov metód */