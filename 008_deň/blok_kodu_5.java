/* Blok kódu Pole */
public class blok_kodu_5 {
    public static void main(String[] args) {
        int[] numbers = {1, 2, 3, 4, 5};
        
        for (int number : numbers) {
            System.out.println("Číslo: " + number);
        }
    }
}
/* Vysvetlenie: Tento kód definuje triedu 'blok_kodu_5' s metódou 'main'.
 * Pole 'numbers' obsahuje päť čísel. V cykle 'for-each' sa prechádza 
 * cez každú hodnotu v poli a vypíše sa na konzolu.
 */