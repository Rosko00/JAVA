/* Blok kódu cykly (for) */
public class blok_kodu_3 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hodnota i: " + i);
        }
    }
}
/* Vysvetlenie: Tento kód definuje triedu 'blok_kodu_3' s metódou 'main'.
 * V bloku 'for' sa premmená 'i' inicializuje na 0 a zvyšuje sa o 1 v každom cykle,
 * až kým nie je menšia ako 5. V každej iterácií sa vypíše aktuálna hodnota 'i'.
 */
