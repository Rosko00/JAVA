/* Identifikátor Metóda */
public class identifikator_2 {
    public static void main(String[] args) {
        // Volanie metódy s identifikátorom "greet"
        greet();
    }

    // Definícia metódy s identifikátorom "greet"
    public static void greet() {
        System.out.println("Ahoj, metóda greet!");
    }
}
