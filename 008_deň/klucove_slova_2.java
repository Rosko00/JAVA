/* Použitie kľúčových slov int, if, else, a return */
public class klucove_slova_2 {
    public static void main(String[] args) {
        int number = 10; // Deklarácia a inicializácia premennej s kľúčovým slovom int
        
        // Podmienkové kľúčové slová if a else
        if (number > 0) {
            System.out.println("Číslo je kladné.");
        } else if (number < 0) {
            System.out.println("Číslo je záporné.");
        } else {
            System.out.println("Číslo je nula.");
        }
    }
}
// int: Označuje celočíselný dátový typ.
// if, else if, else: Kľúčové slová pre podmienkové vyjadrenia.