/* Boolovský dátový typ v Jave je boolean, ktorý môže nadobúdať len dve hodnoty: true alebo false. */
public class boolovske_datove_typy {
        public static void main(String[] args) {
        // Deklarácia a inicializácia boolean premenných
        boolean isJavaFun = true;
        boolean isFishTasty = false;

        // Výpis hodnôt na konzolu
        System.out.println("Je Java zábavná? " + isJavaFun);
        System.out.println("Je ryba chutná? " + isFishTasty);
    }
}
/* V tomto príklade deklarujeme dve boolovské premenné isJavaFun a isFishTasty, ktoré sú inicializované hodnotami true a false. */