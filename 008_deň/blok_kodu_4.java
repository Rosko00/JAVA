/* Metódy */
public class blok_kodu_4 {
    public static void main(String[] args) {
        sayHello();
    }

    public static void sayHello() {
        System.out.println("Ahoj, metóda!");
    }
}
/* Vysvetlenie: Tento kód definuje triedu 'blok_kodu_4' s metou 'main'.
 * Metóda 'main' volá inú metódu 'sayHello', ktorá vypíše text 'Ahoj, metóda!'
 * na terminál. Blok kódu v metóde 'sayHello' je telo tejto metódy.
 */